package temperatura;

public class CelsiusParaFahrenheit {
   public static void main (String [] args) {
	   float dado1; 
	   float dado2;
	   float dado3; 
	   float dado4; 
	   float fahrenheit;
	   
	   dado1 = 9; 
	   dado2 = 27; 
	   dado3 = 160;
	   dado4 = 5; 
	   
	   
	  fahrenheit= (dado1 * dado2 + dado3) / dado4; 
	  
	  System.out.print(fahrenheit);
   }
}
